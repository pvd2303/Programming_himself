# Метод JOIN**********
    # Добавляет новые символы между всеми символами в строке
first_three = "аба"
result = "+".join(first_three)
print(result)
result = " ".join(first_three).upper()
print(result)
    # Обединяет сисок строк в единую строку
words = ["Рыжая",
         "лисица",
         "сделала",
         "кувырок",
         "через",
         "голову",
         "."]
one = "".join(words)
print(one)
one = " ".join(words)
print(one)

# Метод STRIP**********
    # удаление пробельных символов в начале и в конце строки
s = "     Киев     "
s = s.strip()
print(s)

# Метод REPLACE**********
    # Заменяет каждое вхождение строки другой строкой
equ = "Все животные одинаковы."
equ = equ.replace("о", "@")
print(equ)

