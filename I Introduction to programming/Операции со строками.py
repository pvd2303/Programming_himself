# Ключевое слово IN**********
    # Проверяеи содержиться ли строка в другой строке
cat = "Кот"
cat_in_hat = "Кот в шляпе."
print(cat in cat_in_hat)

# Ключевое слово NOT IN**********
    # Проверяеи отсутствие строки в другой строке
mouse = "Мышь"
cat_in_hat = "Кот в шляпе."
print(mouse not in cat_in_hat)

