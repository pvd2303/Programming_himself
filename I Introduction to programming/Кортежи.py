# Получение элементов кортежа
dys = ("1984",
       "О дивный новый мир",
       "451 градус по Фаренгейту")
print(dys)
print(dys[2])

# Проверить наличие элемента в кортеже
print("1984" in dys)

# Проверить отсутствие элемента в кортеже
print("1985" not in dys)