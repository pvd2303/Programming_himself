# Два способа моздания кортежей
my_tuple_1 = tuple()
print(my_tuple_1)

my_tuple_2 = ()
print(my_tuple_2)
# Создание кортежа
rndm = ("М. Джексон", 1958, True)
print(rndm)
# Это кортеж
expl = ("apple",) # ЗАПЯТАЯ в конце обязательна, если 1 элемент
